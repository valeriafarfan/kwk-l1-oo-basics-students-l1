class Book
 
  def initalize(title, author, page_content, genre)
    @title = title
    @author = author
    @page_content = page_content
    @genre = genre
  end
  
  def title
    @title
  end
  
  def title=(title)
    @title = title
  end
  
  def author
    @author
  end
  
  def author=(author)
   @author = author
 end
 
 def page_content
   @page_content
 end
 
 def page_content=(page_content)
   @page_content = page_content
 end
 
 def genre 
   @genre
   end
   
   def genre=(genre)
   @genre = genre
 end
 
 